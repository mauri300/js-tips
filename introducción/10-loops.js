/* Bucle for */
for(let i = 0; i = 0; i++){
    console.log(i);
}

/* Bucle while */
let k = 0;
while(k < 10){
    k++;
}

/* Bucle do while */
let j = 0;
do{
    j++;
} while(j < 10);

/* Bucle for .. of */
const pendientes = ['Tarea', 'Comer', 'Proyecto'];
for (let pendiente of pendientes){
    console.log(pendiente);
}

/* Bucle foreach */
pendientes.forEach(tarea => {
    console.log(tarea);
});

